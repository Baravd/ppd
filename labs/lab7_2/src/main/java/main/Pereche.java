package main;

public class Pereche {
    Integer x;
    Integer y;

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public Pereche(Integer x, Integer y) {

        this.x = x;
        this.y = y;
    }
}
